export const findNearestStation = (lat, lon, stations) => {
    let nearest = null;
    let minDistance = Infinity;

    stations.forEach(station => {
        const coords = STATION_COORDINATES[station.Station];
        if (coords) {
            const dist = Math.sqrt(Math.pow(lat - coords.lat, 2) + Math.pow(lon - coords.lon, 2));
            if (dist < minDistance) {
                minDistance = dist;
                nearest = station;
            }
        }
    });

    return nearest;
};
